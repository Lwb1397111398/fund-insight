def get_level1_sector(sector_name: str) -> str:
    """
    将细分板块名称映射到申万一级行业

    Args:
        sector_name: 细分板块名称（如"生猪养殖"、"快递"、"软饮料"）

    Returns:
        申万一级行业名称（如"农林牧渔"、"交通运输"、"食品饮料"）
        如果没有映射，返回原始名称
    """
    # 精确匹配
    if sector_name in SECTOR_MAPPING:
        return SECTOR_MAPPING[sector_name]

    # 模糊匹配（检查是否包含关键词）
    for key, value in SECTOR_MAPPING.items():
        if key in sector_name or sector_name in key:
            return value

    # 概念板块关键词匹配
    for keyword, value in CONCEPT_KEYWORD_MAPPING.items():
        if keyword in sector_name:
            return value

    # 没有映射，返回原始名称
    return sector_name


